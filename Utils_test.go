func Test_getBucketURLForImage(t *testing.T) {
	user := User{
		ID:       "6f2fd621-eeb6-4477-86b3-42d986697b43",
		Username: "testuser",
		Password: "secret",
	}

	privatePhoto := Photo{
		ID:       "f2dcddbf-576c-4816-b3dd-3b20e5faf716",
		IsPublic: false,
		UserID:   user.ID,
		User:     user,
	}
	privateURL, err := getBucketURLForImage(privatePhoto)
	if err != nil {
		t.Error(err)
	}

	if privateURL == "" {
		t.Errorf("url is empty")
	}

	// Private Image URL should container user-id for bucket name and photo.ID for object name
	if !strings.Contains(privateURL, user.ID) && !strings.Contains(privateURL, privatePhoto.ID) {
		t.Errorf("url is malformed")
	}

	publicPhoto := Photo{
		ID:       "f2dcddbf-576c-4816-b3dd-3b20e5faf716",
		IsPublic: true,
		UserID:   user.ID,
		User:     user,
	}
	publicURL, err := getBucketURLForImage(publicPhoto)

	if publicURL == "" {
		t.Errorf("url is empty")
	}

	// Public Image URL should container shopify-image-repo_public for bucket name and photo.ID for object name
	if !strings.Contains(publicURL, PUBLIC_BUCKET_NAME) && !strings.Contains(publicURL, privatePhoto.ID) {
		t.Errorf("url is malformed")
	}
}

